-- Enable UUID extension (usually already on)
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";


-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
-- TABLE 1: scholarships
-- Core table ??? all scholarship data lives here
-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS scholarships (
  id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  title            TEXT NOT NULL,
  provider         TEXT NOT NULL,              -- org/ministry giving it
  amount           NUMERIC(12, 2) NOT NULL,
  amount_type      TEXT NOT NULL DEFAULT 'annual',  -- monthly/annual/one-time
  deadline         DATE NOT NULL,
  description      TEXT,
  eligibility      TEXT,
  category         TEXT[] DEFAULT '{}',          -- ['SC','ST','OBC','General']
  education_level  TEXT[] DEFAULT '{}',          -- ['10th','12th','UG','PG']
  state            TEXT DEFAULT 'All India',
  gender           TEXT DEFAULT 'All',            -- All/Male/Female
  religion         TEXT DEFAULT 'All',
  income_max       NUMERIC(12, 2),               -- max family income, null = no limit
  documents        TEXT[] DEFAULT '{}',          -- required documents list
  apply_url        TEXT,
  is_featured      BOOLEAN DEFAULT FALSE,
  is_active        BOOLEAN DEFAULT TRUE,
  view_count       INTEGER DEFAULT 0,
  created_at       TIMESTAMPTZ DEFAULT NOW(),
  updated_at       TIMESTAMPTZ DEFAULT NOW()
);

-- Indexes for fast filtering
CREATE INDEX idx_scholarships_deadline   ON scholarships (deadline);
CREATE INDEX idx_scholarships_state       ON scholarships (state);
CREATE INDEX idx_scholarships_active      ON scholarships (is_active);
CREATE INDEX idx_scholarships_featured    ON scholarships (is_featured);
CREATE INDEX idx_scholarships_category    ON scholarships USING GIN(category);
CREATE INDEX idx_scholarships_education   ON scholarships USING GIN(education_level);
-- Full text search index
CREATE INDEX idx_scholarships_search ON scholarships
  USING GIN(to_tsvector('english', title || ' ' || COALESCE(provider, '')));


-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
-- TABLE 2: profiles
-- Extends auth.users with app-specific data
-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS profiles (
  id               UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name        TEXT,
  phone            TEXT,
  education_level  TEXT,                         -- 10th/12th/UG/PG/PhD
  category         TEXT,                         -- SC/ST/OBC/General/EWS
  state            TEXT,
  gender           TEXT,
  income_annual    NUMERIC(12, 2),
  date_of_birth    DATE,
  is_premium       BOOLEAN DEFAULT FALSE,
  premium_until    DATE,
  avatar_url       TEXT,
  onboarded        BOOLEAN DEFAULT FALSE,      -- has user completed onboarding?
  created_at       TIMESTAMPTZ DEFAULT NOW(),
  updated_at       TIMESTAMPTZ DEFAULT NOW()
);

-- Auto-create profile on signup
CREATE OR REPLACE FUNCTION handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO profiles (id, full_name, avatar_url)
  VALUES (
    NEW.id,
    NEW.raw_user_meta_data->>'full_name',
    NEW.raw_user_meta_data->>'avatar_url'
  );
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE handle_new_user();


-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
-- TABLE 3: bookmarks
-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS bookmarks (
  id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id          UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  scholarship_id   UUID NOT NULL REFERENCES scholarships(id) ON DELETE CASCADE,
  created_at       TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id, scholarship_id)           -- no duplicate bookmarks
);

CREATE INDEX idx_bookmarks_user ON bookmarks(user_id);


-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
-- TABLE 4: applications
-- Track user's application journey
-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS applications (
  id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id          UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  scholarship_id   UUID NOT NULL REFERENCES scholarships(id) ON DELETE CASCADE,
  status           TEXT NOT NULL DEFAULT 'planning',
  --  planning ??? applied ??? under_review ??? approved ??? rejected
  applied_at       TIMESTAMPTZ,
  result_at        TIMESTAMPTZ,
  notes            TEXT,
  created_at       TIMESTAMPTZ DEFAULT NOW(),
  updated_at       TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id, scholarship_id)
);

CREATE INDEX idx_applications_user   ON applications(user_id);
CREATE INDEX idx_applications_status ON applications(status);


-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
-- TABLE 5: reminders
-- Scheduled deadline reminders (email/whatsapp)
-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS reminders (
  id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id          UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  scholarship_id   UUID NOT NULL REFERENCES scholarships(id) ON DELETE CASCADE,
  remind_at        TIMESTAMPTZ NOT NULL,        -- when to send the reminder
  channel          TEXT DEFAULT 'email',        -- email/whatsapp/sms
  sent             BOOLEAN DEFAULT FALSE,
  sent_at          TIMESTAMPTZ,
  created_at       TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_reminders_unsent ON reminders(sent, remind_at)
  WHERE sent = FALSE;                          -- partial index: only unsent


-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
-- TABLE 6: featured_listings  (Revenue stream)
-- NGOs/orgs pay to be featured
-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
CREATE TABLE IF NOT EXISTS featured_listings (
  id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  scholarship_id   UUID NOT NULL REFERENCES scholarships(id) ON DELETE CASCADE,
  starts_at        DATE NOT NULL,
  ends_at          DATE NOT NULL,
  amount_paid      NUMERIC(10, 2),
  contact_name     TEXT,
  contact_email    TEXT,
  razorpay_id      TEXT,                         -- payment ref for Phase 3
  created_at       TIMESTAMPTZ DEFAULT NOW()
);


-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
-- Auto-update updated_at on row change
-- ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER scholarships_updated_at
  BEFORE UPDATE ON scholarships
  FOR EACH ROW EXECUTE PROCEDURE update_updated_at();

CREATE TRIGGER profiles_updated_at
  BEFORE UPDATE ON profiles
  FOR EACH ROW EXECUTE PROCEDURE update_updated_at();

CREATE TRIGGER applications_updated_at
  BEFORE UPDATE ON applications
  FOR EACH ROW EXECUTE PROCEDURE update_updated_at();




